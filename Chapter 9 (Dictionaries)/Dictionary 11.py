# Bonus: Two iteraatin variables!

# We loop through the key-value pairs in a dictinary using *two* iteration variables
# Each iteration, the first variables is the key and the second variable is the corresponding value for the key.

jjj = {'chuck': 1, 'fred': 42 , 'jan': 100}
for aaa, bbb in jjj.items():
    print(aaa, bbb)
