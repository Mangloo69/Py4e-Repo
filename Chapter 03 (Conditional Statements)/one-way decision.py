# One-way Decisions
x = 5
print('Before 5')
if x == 5:
    print('Is 5')
    print('Is Still 5')
    print('Third 5')
print('Afterwards 5')
print('Before 6')
if x == 6:        
    print('Is 6')
    print('Is Still 6')
    print('Third 6')
 # this indenting is a way to, in effect, make bigger   blocks of conditional code, or multi-line blocks of conditional code.
    
print('Afterwards 6')
